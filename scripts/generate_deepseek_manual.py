#!/usr/bin/env python3
"""Generate the DeepSeek kernel reference from the pinned migration manifest."""

from __future__ import annotations

import json
import re
import shutil
from pathlib import Path


MARKER = "<!-- Generated by scripts/generate_deepseek_manual.py; do not edit. -->"
MANIFEST = Path("benchmark/one-level-arch/kernels/deepseek/migration.json")
OUTPUT = Path("docs/benchmarks/deepseek")
NAV_START = "          # DEEPSEEK-NAV:START"
NAV_END = "          # DEEPSEEK-NAV:END"


def slug(value: str) -> str:
    return re.sub(r"[^a-z0-9]+", "-", value.lower()).strip("-")


def reader_source(text: str) -> str:
    """Use public tile aliases in the reader-facing copy of translated code."""
    text = text.replace(
        "#include <pto_kernel/common/pto_tileop.hpp>",
        "#include <pto_kernel/tile.hpp>",
    )
    text = text.replace("pto::Tile<pto::Location::Vec,", "pto::LocalTile<")
    text = text.replace("Tile<Location::Vec,", "LocalTile<")
    text = text.replace("TileLeft<", "MatrixLeftTile<")
    text = text.replace("TileRight<", "MatrixRightTile<")
    text = text.replace("TileAcc<", "AccumulatorTile<")
    return text


def code_fence(text: str, title: str) -> str:
    text = reader_source(re.sub(r"/\*.*?\*/", "", text, flags=re.DOTALL))
    text = "\n".join(re.sub(r"\s*//.*$", "", line).rstrip() for line in text.splitlines())
    text = re.sub(r"\n{3,}", "\n\n", text).strip()
    return f'```cpp title="{title}" linenums="1"\n{text}\n```'


def central_function(text: str, names: list[str]) -> str:
    """Extract the first named function, including a preceding template line."""
    lines = text.splitlines()
    candidates = names or [""]
    start = None
    for name in candidates:
        pattern = re.compile(rf"\b{re.escape(name)}\s*\(") if name else re.compile(r"\bvoid\s+\w+\s*\(")
        for index, line in enumerate(lines):
            if pattern.search(line):
                start = index
                while start > 0 and (
                    lines[start - 1].lstrip().startswith("template")
                    or lines[start - 1].lstrip().startswith("//")
                ):
                    start -= 1
                break
        if start is not None:
            break
    if start is None:
        return "\n".join(lines[: min(120, len(lines))])

    depth = 0
    opened = False
    end = start
    for index in range(start, len(lines)):
        line = re.sub(r'"(?:\\.|[^"\\])*"', '""', lines[index])
        depth += line.count("{") - line.count("}")
        opened = opened or "{" in line
        end = index
        if opened and depth == 0:
            break
    return "\n".join(lines[start : end + 1])


def relative_source_link(source: str, revision: str) -> str:
    return f"https://github.com/deepseek-ai/TileKernels/blob/{revision}/{source}"


def render_page(root: Path, entry: dict[str, object], revision: str) -> tuple[Path, str]:
    family = str(entry["family"])
    source = str(entry["source"])
    implementations = [str(path) for path in entry["implementations"]]
    primary = root / implementations[0]
    primary_text = primary.read_text(encoding="utf-8")
    names = [str(name) for name in entry.get("functions", [])]
    excerpt = central_function(primary_text, names)
    page = Path(f"{family}/{slug(Path(source).stem.removesuffix('_kernel'))}.md")
    source_url = relative_source_link(source, revision)
    intrinsics = [str(name) for name in entry["intrinsics"]]
    links = ", ".join(
        f"[`{name}`](../../../intrinsics/{name.lower()}.md)" for name in intrinsics
    ) or "No tile operation is required by this helper path."
    function_names = ", ".join(f"`{name}`" for name in names) or "See implementation"

    lines = [
        f"# {entry['title']}",
        "",
        MARKER,
        "",
        str(entry["summary"]),
        "",
        "| Field | Value |",
        "| --- | --- |",
        f"| Upstream module | [`{source}`]({source_url}) |",
        f"| Pinned revision | `{revision}` |",
        f"| C++ function | {function_names} |",
        f"| Verification | `{entry['verification']}` |",
        "| Minimum thread fragment | 128 bytes |",
        "",
        "## Core C++ kernel",
        "",
        code_fence(excerpt, primary.name),
        "",
        "## Algorithm walkthrough",
        "",
        str(entry["algorithm"]),
        "",
        "## Source-to-intrinsic mapping",
        "",
        links,
        "",
        "Scalar C++ controls dimensions, loop bounds, and tile coordinates. Tensor",
        "values remain in global tensor views and typed tiles throughout the data path.",
        "",
        "## Block and thread partition",
        "",
        str(entry["partition"]),
        "",
        "## Memory and tile flow",
        "",
        str(entry["tile_flow"]),
        "",
        "## Build and verification",
        "",
        "Run the migration gate from the repository root:",
        "",
        "```console",
        "python3 scripts/verify_deepseek_migration.py",
        "```",
        "",
        "The verification label records the strongest completed gate. A",
        "`source-validated` page is a valid API example even when compiler lowering",
        "for one of its public intrinsics is not yet available.",
        "",
        "## Complete translated source",
        "",
    ]
    for implementation in implementations:
        path = root / implementation
        text = path.read_text(encoding="utf-8")
        lines.extend(
            [
                f'??? code "{path.name}"',
                "",
                *[("    " + line) if line else "" for line in code_fence(text, path.name).splitlines()],
                "",
            ]
        )
    return page, "\n".join(lines).rstrip() + "\n"


def main() -> None:
    root = Path(__file__).resolve().parents[1]
    manifest = json.loads((root / MANIFEST).read_text(encoding="utf-8"))
    revision = str(manifest["upstream"]["revision"])
    output = root / OUTPUT
    if output.exists():
        shutil.rmtree(output)
    output.mkdir(parents=True)

    family_rows: dict[str, list[tuple[dict[str, object], Path]]] = {}
    for entry in manifest["kernel_modules"]:
        page, text = render_page(root, entry, revision)
        target = output / page
        target.parent.mkdir(parents=True, exist_ok=True)
        target.write_text(text, encoding="utf-8")
        family_rows.setdefault(str(entry["family"]), []).append((entry, page))

    lines = [
        "# DeepSeek kernel reference",
        "",
        MARKER,
        "",
        "This reference accounts for all **37 standalone kernel modules** in the",
        "pinned DeepSeek TileKernels source tree. Helper-only modules are dependencies",
        "and do not receive standalone pages.",
        "",
        f"**Pinned revision:** `{revision}`",
        "",
    ]
    for family, rows in family_rows.items():
        lines.extend([f"## {family.title()}", "", "| Kernel | Source module | Status |", "| --- | --- | --- |"])
        for entry, page in sorted(rows, key=lambda item: str(item[0]["title"])):
            link = page.relative_to(Path(".")).as_posix()
            lines.append(
                f"| [{entry['title']}]({link}) | `{entry['source']}` | `{entry['verification']}` |"
            )
        lines.append("")
    (output / "index.md").write_text("\n".join(lines).rstrip() + "\n", encoding="utf-8")
    nav_lines = [NAV_START, "          - Overview: benchmarks/deepseek/index.md"]
    for family, rows in family_rows.items():
        nav_lines.append(f"          - {family.title()}:")
        for entry, page in sorted(rows, key=lambda item: str(item[0]["title"])):
            nav_lines.append(f"              - {entry['title']}: benchmarks/deepseek/{page.as_posix()}")
    nav_lines.append(NAV_END)
    nav_path = root / "mkdocs.yml"
    nav = nav_path.read_text(encoding="utf-8")
    pattern = re.escape(NAV_START) + r".*?" + re.escape(NAV_END)
    if not re.search(pattern, nav, flags=re.DOTALL):
        raise ValueError("mkdocs.yml lacks DeepSeek navigation markers")
    nav_path.write_text(
        re.sub(pattern, "\n".join(nav_lines), nav, flags=re.DOTALL),
        encoding="utf-8",
    )
    print(f"Generated {sum(len(rows) for rows in family_rows.values())} DeepSeek kernel pages.")


if __name__ == "__main__":
    main()
