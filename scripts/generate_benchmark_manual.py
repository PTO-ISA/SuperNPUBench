from dataclasses import dataclass
from pathlib import Path


MARKER = "<!-- Generated by scripts/generate_benchmark_manual.py; do not edit. -->"
SOURCE_SUFFIXES = {".c", ".cc", ".cpp", ".h", ".hpp"}
CALL = re.compile(
    r"\b((?:T|M)[A-Z][A-Z0-9_]*|ACCCVT|MATMUL|MATMACC)\s*"
    r"(?:<[^;{}()]*>)?\s*\("
)


@dataclass(frozen=True)
class Build:
    manifest: Path
    line: int
    command: str
    testcase: str
    source: Path
    backend: str
    family: str


def slug(value: str) -> str:
    return re.sub(r"[^a-z0-9]+", "-", value.lower()).strip("-")


def rel(path: Path, root: Path) -> str:
    return path.resolve().relative_to(root.resolve()).as_posix()


def logical_lines(text: str) -> list[tuple[int, str]]:
    """Join shell continuations while retaining the first physical line number."""
    lines: list[tuple[int, str]] = []
    parts: list[str] = []
    start = 1
    for line_number, physical in enumerate(text.splitlines(), 1):
        if not parts:
            start = line_number
        stripped = physical.rstrip()
        if stripped.endswith("\\"):
            parts.append(stripped[:-1].strip())
            continue
        parts.append(physical.strip())
        lines.append((start, " ".join(part for part in parts if part)))
        parts = []
    if parts:
        lines.append((start, " ".join(part for part in parts if part)))
    return lines


def shell_environments(text: str) -> list[dict[str, str]]:
    """Expand the simple finite `for`/`if` constructs used by compile.all files."""
    domains = [
        (match.group(1), shlex.split(match.group(2)))
        for match in re.finditer(
            r"^\s*for\s+([A-Za-z_]\w*)\s+in\s+([^;]+);\s*do\s*$",
            text,
            re.MULTILINE,
        )
    ]
    if not domains:
        return [{}]

    conditionals = list(
        re.finditer(
            r"if\s+\[\[\s+\"\$([A-Za-z_]\w*)\"\s+==\s+\"([^\"]*)\"\s+\]\];"
            r"\s*then\s+([A-Za-z_]\w*)=\"([^\"]*)\"\s*else\s+\3=\"([^\"]*)\"\s*fi",
            text,
            re.DOTALL,
        )
    )
    environments = []
    for values in itertools.product(*(values for _, values in domains)):
        environment = dict(zip((name for name, _ in domains), values))
        for conditional in conditionals:
            source, expected, target, true_value, false_value = conditional.groups()
            environment[target] = (
                true_value if environment.get(source) == expected else false_value
            )
        environments.append(environment)
    return environments


def expand_shell_variables(value: str, environment: dict[str, str]) -> str:
    def replace(match: re.Match[str]) -> str:
        name = match.group(1) or match.group(2)
        return environment.get(name, match.group(0))

    return re.sub(r"\$\{([A-Za-z_]\w*)\}|\$([A-Za-z_]\w*)", replace, value)


def active_commands(manifest: Path) -> list[tuple[int, str, str]]:
    commands = []
    text = manifest.read_text(encoding="utf-8", errors="replace")
    environments = shell_environments(text)
    for line_number, line in logical_lines(text):
        line = line.split("#", 1)[0]
        for match in re.finditer(r"(?:^|;)\s*make\s+([^;]+)", line):
            raw_command = "make " + " ".join(match.group(1).split())
            variables = set(
                (left or right)
                for left, right in re.findall(
                    r"\$\{([A-Za-z_]\w*)\}|\$([A-Za-z_]\w*)", raw_command
                )
            )
            selected = (
                environments
                if variables & set().union(*(environment.keys() for environment in environments))
                else [{}]
            )
            for environment in selected:
                command = expand_shell_variables(raw_command, environment)
                testcase = re.search(r"(?:^|\s)TESTCASE=([^\s]+)", command)
                if testcase:
                    commands.append((line_number, command, testcase.group(1)))
    return commands


def command_values(command: str) -> dict[str, str]:
    values: dict[str, str] = {}
    try:
        tokens = shlex.split(command)
    except ValueError:
        tokens = command.split()
    for token in tokens[1:]:
        if "=" in token:
            key, value = token.split("=", 1)
            values[key] = value
    return values


def source_files(directory: Path) -> list[Path]:
    return sorted(
        path
        for path in directory.rglob("*")
        if path.is_file() and path.suffix in {".c", ".cc", ".cpp"}
    )


def resolve_source(manifest: Path, testcase: str, command: str) -> Path:
    directory = manifest.parent
    files = source_files(directory / "src") if (directory / "src").is_dir() else []
    files += source_files(directory / testcase) if (directory / testcase).is_dir() else []
    files = list(dict.fromkeys(files))

    values = command_values(command)
    special: str | None = None
    if directory.name == "matmul":
        special = {
            "HIF4_HIF4": "HiF4_HiF4.cpp",
            "A16W4": "A16W4.cpp",
            "MASK": "matmul.cpp",
        }.get(values.get("TYPE", ""))
    elif directory.name == "fa":
        special = {
            "fa_HIF4_HIF4": "fa_hif4.cpp",
            "fa_2d_unroll": "fa_2d_unroll.cpp",
            "fa_softmax_pto": "fa_softmax_pto.cpp",
            "fa_2d_unroll_gmma": "fa_2d_unroll_gmma.cpp",
        }.get(testcase)
    if special:
        matches = [path for path in files if path.name == special]
        if len(matches) == 1:
            return matches[0]

    direct = [path for path in files if path.stem.lower() == testcase.lower()]
    if len(direct) == 1:
        return direct[0]

    makefile = directory / "Makefile"
    if makefile.is_file():
        names = {
            Path(value).name
            for value in re.findall(
                r"([A-Za-z0-9_./$(){}-]+\.(?:cpp|cc|c))",
                makefile.read_text(encoding="utf-8", errors="replace"),
            )
        }
        matches = [path for path in files if path.name in names]
        if len(matches) == 1:
            return matches[0]

    raise ValueError(
        f"cannot resolve TESTCASE={testcase} in {manifest}; candidates: "
        + ", ".join(path.name for path in files)
    )


def classify(manifest: Path, root: Path) -> tuple[str, str]:
    path = rel(manifest, root)
    if not path.startswith("benchmark/one-level-arch/test/kernel/"):
        raise ValueError(f"not a one-level benchmark manifest: {path}")
    parts = path.split("/test/kernel/", 1)[1].split("/")[:-1]
    family = "/".join(parts)
    return "One-level", family


def inventory(root: Path) -> list[Build]:
    manifests = sorted(root.glob("benchmark/one-level-arch/test/kernel/**/compile.all"))
    builds: list[Build] = []
    for manifest in manifests:
        backend, family = classify(manifest, root)
        for line, command, testcase in active_commands(manifest):
            builds.append(
                Build(
                    manifest=manifest,
                    line=line,
                    command=command,
                    testcase=testcase,
                    source=resolve_source(manifest, testcase, command),
                    backend=backend,
                    family=family,
                )
            )
    return builds


def file_index(root: Path) -> tuple[list[Path], dict[str, list[Path]]]:
    roots = [
        root / "benchmark" / "one-level-arch",
        # One-level Makefile.common still uses this compatibility include tree.
        root / "benchmark" / "two-level-arch" / "include",
    ]
    files = [
        path
        for source_root in roots
        for path in source_root.rglob("*")
        if path.is_file() and path.suffix in SOURCE_SUFFIXES
    ]
    by_name: dict[str, list[Path]] = defaultdict(list)
    for path in files:
        by_name[path.name].append(path)
    return files, by_name


def resolve_include(
    source: Path, target: str, root: Path, by_name: dict[str, list[Path]]
) -> Path | None:
    candidates = [source.parent / target, root / target]
    path_text = rel(source, root)
    backend_root = root / "benchmark" / "one-level-arch"
    include_roots = [backend_root, backend_root / "include", backend_root / "kernels"]
    include_roots.extend(
        [
            backend_root / "test" / "common",
            backend_root / "test" / "common" / "src",
            root / "benchmark" / "two-level-arch" / "include",
        ]
    )
    candidates.extend(include_root / target for include_root in include_roots)
    for candidate in candidates:
        try:
            resolved = candidate.resolve()
            resolved.relative_to(root.resolve())
        except (FileNotFoundError, ValueError):
            continue
        if resolved.is_file():
            return resolved

    same_name = by_name.get(Path(target).name, [])
    same_backend = [path for path in same_name if str(path).startswith(str(backend_root))]
    choices = same_backend or same_name
    if len(choices) == 1:
        return choices[0]
    return None


def include_closure(source: Path, root: Path, by_name: dict[str, list[Path]]) -> list[Path]:
    seen: set[Path] = set()
    pending = [source]
    while pending:
        current = pending.pop()
        if current in seen:
            continue
        seen.add(current)
        text = current.read_text(encoding="utf-8", errors="replace")
        for target in re.findall(r"^\s*#\s*include\s*[<\"]([^>\"]+)[>\"]", text, re.MULTILINE):
            included = resolve_include(current, target, root, by_name)
            if included and included not in seen:
                pending.append(included)
    return sorted(seen)


def canonical_name(call: str, canonical: set[str]) -> str | None:
    if call in canonical:
        return call
    aliases = {
        "ACCCVT": "TCVT",
        "MATMUL": "TMATMUL",
        "MATMACC": "TMATMUL_ACC",
        "TCAST": "TCVT",
        "TEXPANDCOL": "TCOLEXPAND",
        "TEXPANDROW": "TROWEXPAND",
        "TEXPANDSCALAR": "TEXPANDS",
        "TPAD": "TFILLPAD",
        "TSELECT": "TSEL",
        "TASSEMBLE": "TCONCAT",
        "TROWMAXEXPAND": "TROWEXPANDMAX",
        "MGATHER_MASK": "MGATHER",
        "MSCATTER_MASK": "MSCATTER",
    }
    if call in aliases:
        return aliases[call]
    if re.fullmatch(r"TLOAD(?:\d+)?(?:_.*)?", call):
        return "TLOAD"
    if re.fullmatch(r"TSTORE(?:_ACC)?(?:_DYNAMIC)?", call):
        return "TSTORE"
    if call.startswith("TMOV_"):
        return "TMOV"
    if call.startswith("TCVT_") or call.startswith("TCAST_"):
        return "TCVT"
    for suffix in ("_TEPL", "_V057", "_ASM"):
        if call.endswith(suffix) and call[: -len(suffix)] in canonical:
            return call[: -len(suffix)]
    return None


def calls_for_files(
    files: list[Path], canonical: set[str]
) -> tuple[dict[str, set[Path]], dict[str, set[Path]]]:
    intrinsic_files: dict[str, set[Path]] = defaultdict(set)
    helper_files: dict[str, set[Path]] = defaultdict(set)
    for path in files:
        text = path.read_text(encoding="utf-8", errors="replace")
        text = re.sub(r"/\*.*?\*/", "", text, flags=re.DOTALL)
        text = re.sub(r"//[^\n]*", "", text)
        for match in CALL.finditer(text):
            call = match.group(1)
            name = canonical_name(call, canonical)
            if name:
                intrinsic_files[name].add(path)
            else:
                helper_files[call].add(path)
    return intrinsic_files, helper_files


def usage_files(source: Path, manifest: Path, closure: list[Path], root: Path) -> list[Path]:
    """Keep files that express this benchmark, not shared API declarations."""
    local_root = manifest.parent.resolve()
    selected = []
    for path in closure:
        resolved = path.resolve()
        path_text = rel(path, root)
        is_local = resolved == source.resolve() or local_root in resolved.parents
        is_kernel = "/kernels/" in f"/{path_text}"
        if is_local or is_kernel:
            selected.append(path)
    return selected


def operation_shape(names: set[str]) -> list[str]:
    stages = []
    if names & {"TLOAD", "MGATHER", "TPREFETCH"}:
        stages.append("loads or gathers global data into typed tiles")
    if names & {"TMATMUL", "TMATMUL_ACC", "TMATMUL_BIAS", "TMATMUL_MX", "TGEMV"}:
        stages.append("executes matrix or matrix-vector work on cube/accumulator tiles")
    if any(name.startswith(("TROW", "TCOL")) for name in names):
        stages.append("reduces or broadcasts along tile axes")
    if names & {"TADD", "TSUB", "TMUL", "TDIV", "TEXP", "TRECIP", "TMAX", "TMIN"}:
        stages.append("applies vector elementwise arithmetic")
    if names & {"TTRANS", "TRESHAPE", "TEXTRACT", "TINSERT", "TCONCAT", "TGATHER", "TSCATTER"}:
        stages.append("transforms tile layout or selects tile elements")
    if names & {"TSTORE", "MSCATTER"}:
        stages.append("commits the result to global memory")
    return stages


def intrinsic_spellings(
    intrinsic_files: dict[str, set[Path]],
) -> dict[str, list[str]]:
    canonical = set(intrinsic_files)
    spellings: dict[str, set[str]] = defaultdict(set)
    for name, paths in intrinsic_files.items():
        for path in paths:
            text = path.read_text(encoding="utf-8", errors="replace")
            text = re.sub(r"/\*.*?\*/", "", text, flags=re.DOTALL)
            text = re.sub(r"//[^\n]*", "", text)
            for match in CALL.finditer(text):
                call = match.group(1)
                if canonical_name(call, canonical) == name:
                    spellings[name].add(call)
    return {name: sorted(values) for name, values in spellings.items()}


def code_fence(path: Path) -> str:
    language = "c" if path.suffix == ".c" else "cpp"
    text = "\n".join(
        line.rstrip()
        for line in path.read_text(encoding="utf-8", errors="replace").splitlines()
    ).rstrip()
    return f"```{language} title=\"{path.name}\" linenums=\"1\"\n{text}\n```"


def intrinsic_link(name: str, page_depth: int = 4) -> str:
    return "../" * page_depth + f"intrinsics/{name.lower()}.md"


def implementation_page(
    output: Path,
    root: Path,
    source: Path,
    builds: list[Build],
    closure: list[Path],
    intrinsic_files: dict[str, set[Path]],
    helper_files: dict[str, set[Path]],
) -> str:
    source_hash = hashlib.sha1(rel(source, root).encode()).hexdigest()[:8]
    page_name = f"{slug(source.stem)}-{source_hash}.md"
    title = f"{builds[0].testcase}: {source.name}"
    names = set(intrinsic_files)
    spellings = intrinsic_spellings(intrinsic_files)
    lines = [
        f"# {title}",
        "",
        MARKER,
        "",
        "| Field | Value |",
        "| --- | --- |",
        f"| Benchmark surface | {builds[0].backend} |",
        f"| Family | `{builds[0].family}` |",
        f"| Implementation source | `{rel(source, root)}` |",
        f"| Active build variants | {len(builds)} |",
        "",
        "## How the Code Is Written",
        "",
    ]
    stages = operation_shape(names)
    if stages:
        lines.append("The implementation " + ", then ".join(stages) + ".")
    else:
        lines.append(
            "This entry is a benchmark driver or wrapper. Its PTO work is expressed by "
            "the local implementation files shown below."
        )
    lines.extend(["", code_fence(source), ""])

    pto_files = sorted(
        {path for paths in intrinsic_files.values() for path in paths if path != source}
    )
    if pto_files:
        lines.extend(
            [
                "## PTO-Bearing Local Implementations",
                "",
                "These local include files contain the PTO calls reached by this entrypoint.",
                "",
            ]
        )
        for path in pto_files:
            lines.extend(
                [
                    f"??? code \"{rel(path, root)}\"",
                    "",
                    *[
                        "    " + line if line else ""
                        for line in code_fence(path).splitlines()
                    ],
                    "",
                ]
            )

    lines.extend(["## Supported PTO Intrinsics", ""])
    if intrinsic_files:
        lines.extend(
            [
                "This is the source-level union across the compile-time paths in this",
                "implementation. A build command may select a subset through its macros;",
                "the exact source spellings below preserve aliases and masked forms.",
                "",
                "| Intrinsic contract | Source spelling | Called from |",
                "| --- | --- | --- |",
            ]
        )
        for name, paths in sorted(intrinsic_files.items()):
            origins = "<br>".join(f"`{rel(path, root)}`" for path in sorted(paths))
            forms = ", ".join(f"`{form}`" for form in spellings.get(name, [name]))
            lines.append(
                f"| [`{name}`]({intrinsic_link(name)}) | {forms} | {origins} |"
            )
        lines.append("")
    else:
        lines.extend(
            [
                "No PTO tile intrinsic is called by this implementation or its resolved",
                "local include closure.",
                "",
            ]
        )
    if helper_files:
        lines.extend(
            [
                "### Repository Helper Surface",
                "",
                "These uppercase calls are repository wrappers or fused helpers, not distinct",
                "entries in the current PTO ISA manual.",
                "",
                "| Helper | Defined or called from |",
                "| --- | --- |",
            ]
        )
        for name, paths in sorted(helper_files.items()):
            origins = "<br>".join(f"`{rel(path, root)}`" for path in sorted(paths))
            lines.append(f"| `{name}` | {origins} |")
        lines.append("")

    lines.extend(
        [
            "## Active Build Commands",
            "",
            f"Run these from `{rel(builds[0].manifest.parent, root)}` after setting",
            "`COMPILER_DIR` and `LINX_SYSROOT` as described in the build guide.",
            "",
            "| Manifest line | Command |",
            "| ---: | --- |",
        ]
    )
    for build in builds:
        command = build.command.replace("|", "\\|")
        lines.append(f"| {build.line} | `{command}` |")
    lines.append("")

    data_files = sorted(
        path
        for path in builds[0].manifest.parent.rglob("data_obj/*")
        if path.is_file() and path.name != ".gitignore"
    )
    if data_files:
        lines.extend(
            [
                "## Required Data Objects",
                "",
                "The benchmark build or run path uses these checked-in generators/data inputs:",
                "",
            ]
        )
        lines.extend(f"- `{rel(path, root)}`" for path in data_files)
        lines.append("")

    published_closure = [
        path
        for path in closure
        if rel(path, root).startswith("benchmark/one-level-arch/")
    ]
    lines.extend(
        [
            "## Resolved One-Level Source Closure",
            "",
            f"??? info \"{len(published_closure)} one-level source files reached through local includes\"",
            "",
        ]
    )
    lines.extend(f"    - `{rel(path, root)}`" for path in published_closure)
    lines.append("")
    (output / page_name).write_text("\n".join(lines), encoding="utf-8")
    return page_name


def write_readme_catalog(root: Path, builds: list[Build]) -> None:
    start = "<!-- BENCHMARK-CATALOG:START -->"
    end = "<!-- BENCHMARK-CATALOG:END -->"
    grouped: dict[tuple[str, str], list[Build]] = defaultdict(list)
    for build in builds:
        grouped[(build.backend, build.family)].append(build)
    lines = [
        start,
        "## One-level benchmark catalog",
        "",
        f"The one-level manifests contain **{len(builds)} build variants**. Every name below",
        "has a source-complete page with its build command and PTO intrinsic surface in",
        "the website's **Benchmarks** section.",
        "",
    ]
    for (backend, family), rows in sorted(grouped.items()):
        names = sorted({build.testcase for build in rows}, key=str.lower)
        lines.extend(
            [
                f"<details><summary><strong>{backend} / {family}</strong> "
                f"({len(names)} {'name' if len(names) == 1 else 'names'}, "
                f"{len(rows)} {'variant' if len(rows) == 1 else 'variants'})</summary>",
                "",
                ", ".join(f"`{name}`" for name in names),
                "",
                "</details>",
                "",
            ]
        )
    lines.append(end)
    section = "\n".join(lines)
    readme = root / "README.md"
    text = readme.read_text(encoding="utf-8")
    if start in text and end in text:
        text = re.sub(
            re.escape(start) + r".*?" + re.escape(end),
            section,
            text,
            flags=re.DOTALL,
        )
    else:
        text = text.replace("\n## Models\n", "\n" + section + "\n\n## Models\n")
    readme.write_text(text, encoding="utf-8")


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--repo-root", type=Path, default=Path(__file__).resolve().parents[1]
    )
    args = parser.parse_args()
    root = args.repo_root.resolve()
    output_root = root / "docs" / "benchmarks" / "catalog"
    if output_root.exists():
        shutil.rmtree(output_root)
    output_root.mkdir(parents=True)

    canonical = {
        path.stem.upper()
        for path in (root / "docs" / "intrinsics").glob("*.md")
        if re.fullmatch(r"(?:t[a-z0-9_]+|mgather|mscatter)", path.stem)
    }
    if not canonical:
        raise SystemExit("run scripts/sync_golden_manual.py first")

    builds = inventory(root)
    _, by_name = file_index(root)
    grouped: dict[tuple[str, str, Path], list[Build]] = defaultdict(list)
    for build in builds:
        grouped[(build.backend, build.family, build.source)].append(build)

    family_rows: dict[tuple[str, str], list[dict[str, object]]] = defaultdict(list)
    json_builds = []
    for (backend, family, source), source_builds in sorted(
        grouped.items(), key=lambda item: (item[0][0], item[0][1], str(item[0][2]))
    ):
        family_dir = output_root / slug(backend) / slug(family)
        family_dir.mkdir(parents=True, exist_ok=True)
        closure = include_closure(source, root, by_name)
        evidence_files = usage_files(source, source_builds[0].manifest, closure, root)
        intrinsic_files, helper_files = calls_for_files(evidence_files, canonical)
        page_name = implementation_page(
            family_dir,
            root,
            source,
            source_builds,
            closure,
            intrinsic_files,
            helper_files,
        )
        forms = intrinsic_spellings(intrinsic_files)
        row = {
            "page": page_name,
            "source": rel(source, root),
            "testcases": sorted({build.testcase for build in source_builds}),
            "variants": len(source_builds),
            "intrinsics": sorted(intrinsic_files),
            "intrinsic_forms": forms,
            "helpers": sorted(helper_files),
        }
        family_rows[(backend, family)].append(row)
        for build in source_builds:
            json_builds.append(
                {
                    "backend": backend,
                    "family": family,
                    "manifest": rel(build.manifest, root),
                    "manifest_line": build.line,
                    "command": build.command,
                    "testcase": build.testcase,
                    "source": rel(source, root),
                    "page": rel(family_dir / page_name, root / "docs"),
                    "intrinsic_surface_scope": "source-union",
                    "source_intrinsics": sorted(intrinsic_files),
                    "intrinsic_forms": forms,
                    "helpers": sorted(helper_files),
                }
            )

    catalog_rows = []
    for (backend, family), rows in sorted(family_rows.items()):
        family_dir = output_root / slug(backend) / slug(family)
        intrinsic_union = sorted({name for row in rows for name in row["intrinsics"]})
        lines = [
            f"# {backend}: {family}",
            "",
            MARKER,
            "",
            f"This family contains **{len(rows)}** source implementations and",
            f"**{sum(int(row['variants']) for row in rows)}** active build variants.",
            "",
            "| Implementation | Source | Variants | PTO source-union surface |",
            "| --- | --- | ---: | --- |",
        ]
        for row in rows:
            names = (
                ", ".join(
                    f"[`{name}`]({intrinsic_link(name)})"
                    for name in row["intrinsics"]
                )
                or "None"
            )
            label = ", ".join(row["testcases"])
            lines.append(
                f"| [{label}]({row['page']}) | `{row['source']}` | {row['variants']} | {names} |"
            )
        lines.append("")
        (family_dir / "index.md").write_text("\n".join(lines), encoding="utf-8")
        catalog_rows.append(
            {
                "backend": backend,
                "family": family,
                "page": rel(family_dir / "index.md", root / "docs"),
                "implementations": len(rows),
                "variants": sum(int(row["variants"]) for row in rows),
                "intrinsics": intrinsic_union,
            }
        )

    index_lines = [
        "# Benchmark Programming Catalog",
        "",
        MARKER,
        "",
        "This catalog is generated from every active one-level `compile.all` command. Each",
        "implementation page presents the complete entry source, PTO-bearing local",
        "include implementations, the canonical PTO intrinsic surface, exact build",
        "commands, and required data objects.",
        "",
        f"**Coverage:** {len(builds)} active build variants, {len(grouped)} source-backed",
        f"implementations, and {len(family_rows)} benchmark families.",
        "",
        "| Surface | Family | Implementations | Build variants | PTO source-union surface |",
        "| --- | --- | ---: | ---: | --- |",
    ]
    for row in catalog_rows:
        count = len(row["intrinsics"])
        names = f"{count} intrinsic{'s' if count != 1 else ''}" if count else "None"
        index_lines.append(
            f"| {row['backend']} | [{row['family']}]({row['page'].removeprefix('benchmarks/')}) "
            f"| {row['implementations']} | {row['variants']} | {names} |"
        )
    index_lines.extend(
        [
            "",
            "## Reading a Benchmark Page",
            "",
            "1. Start with **How the Code Is Written** for the complete entry source.",
            "2. Expand **PTO-Bearing Local Implementations** to inspect the reached kernel code.",
            "3. Use **Supported PTO Intrinsics** to open the normative intrinsic contract.",
            "4. Reproduce a listed command from the directory shown on the page.",
            "",
            "Only `benchmark/one-level-arch` entrypoints are published here. Other repository",
            "surfaces are outside this programming-model profile.",
            "",
        ]
    )
    (root / "docs" / "benchmarks" / "index.md").write_text(
        "\n".join(index_lines), encoding="utf-8"
    )
    payload = {
        "schema": 3,
        "active_build_variants": len(builds),
        "source_implementations": len(grouped),
        "families": catalog_rows,
        "builds": sorted(json_builds, key=lambda row: (row["manifest"], row["manifest_line"], row["command"])),
    }
    (root / "docs" / "benchmarks" / "catalog.json").write_text(
        json.dumps(payload, indent=2) + "\n", encoding="utf-8"
    )
    write_readme_catalog(root, builds)
    print(
        f"Generated {len(grouped)} implementation pages for {len(builds)} active "
        f"build variants across {len(family_rows)} families."
    )


