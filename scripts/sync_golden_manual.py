def normalize_instruction(text: str, *, communication: bool) -> str:
    text = remove_diagram_section(text).strip() + "\n"
    text = rewrite_links(text, communication=communication)
    text = rewrite_manual_paths(text, communication=communication)
    text = re.sub(
        r"Declared in `include/[^`]+`(?:\s+and\s+`include/[^`]+`)*[.:]?\s*",
        "C++ declaration:\n\n",
        text,
    )
    text = re.sub(
        r"\s*\((?:see|See) `include/pto/[^`]+`\)",
        "",
        text,
    )
    text = re.sub(
        r"(?:See|see) backend headers under `include/pto/[^`]+`(?: for [^.]+)?\.",
        "The selected target defines any additional implementation constraints.",
        text,
    )
    text = re.sub(
        r"(?:See|see) `include/pto/[^`]+`(?: for [^.]+)?\.",
        "The selected target defines any additional implementation constraints.",
        text,
    )
    lines = text.splitlines()
    if lines and lines[0].startswith("# "):
        lines.insert(1, "")
        lines.insert(2, MARKER)
    text = "\n".join(lines).rstrip() + "\n"

    relative_targets = re.findall(r"\]\(((?:\.\./){1,}[^)]+)\)", text)
    bad_links = [
        target
        for target in relative_targets
        if not re.fullmatch(
            r"(?:\.\./)+(?:assembly|index|[a-z0-9_]+|"
            r"tutorials/tile-kernel|start/troubleshooting)\.md(?:#.*)?",
            target,
        )
    ]
    if bad_links:
        raise ValueError(f"unresolved relative links: {bad_links[:3]}")
    if re.search(r"!\[[^]]*\]\([^)]*\)", text):
        raise ValueError("instruction page still contains an unavailable image")
    return text


def normalize_assembly(text: str) -> str:
    text = text.strip() + "\n"
    text = re.sub(r"\]\((?:\.\./)+pto/([A-Z][A-Z0-9_]+)\.md\)",
                  lambda m: f"]({m.group(1).lower()}.md)", text)
    text = re.sub(r"\]\((?:\.\./)+[^)]+\)", "](#)", text)
    lines = text.splitlines()
    if lines and lines[0].startswith("# "):
        lines.insert(1, "")
        lines.insert(2, MARKER)
    return "\n".join(lines).rstrip() + "\n"


