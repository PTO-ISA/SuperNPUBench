def git_output(root: Path, *args: str) -> str:
    return subprocess.check_output(
        ["git", "-C", str(root), *args], text=True
    ).strip()


def read(path: Path) -> str:
    return path.read_text(encoding="utf-8-sig").replace("\r\n", "\n")


def read_allowlist() -> list[str]:
    names = [
        line.strip()
        for line in read(ALLOWLIST_PATH).splitlines()
        if line.strip() and not line.lstrip().startswith("#")
    ]
    if len(names) != 111 or len(set(names)) != len(names):
        raise ValueError(
            f"expected 111 unique LinxISA 0.57 intrinsics, found {len(names)}"
        )
    invalid = [name for name in names if not INSTRUCTION_NAME.fullmatch(name)]
    if invalid:
        raise ValueError(f"invalid LinxISA 0.57 intrinsic names: {invalid}")
    return names


def instruction_pages(directory: Path) -> dict[str, Path]:
    return {
        path.stem: path
        for path in directory.glob("*.md")
        if INSTRUCTION_NAME.fullmatch(path.stem)
    }


def parse_categories(
    index_path: Path, pages: dict[str, Path]
) -> tuple[OrderedDict[str, list[str]], dict[str, str]]:
    categories: OrderedDict[str, list[str]] = OrderedDict()
    summaries: dict[str, str] = {}
    category = "Additional current instructions"

    for line in read(index_path).splitlines():
        heading = re.fullmatch(r"## (.+)", line.strip())
        if heading:
            category = heading.group(1)
            continue

        match = re.match(
            r"- \[(?:\*\*)?([^\]*]+?)(?:\*\*)?\]\(([^)]+\.md)\)(?::| -)?\s*(.*)",
            line.strip(),
        )
        if not match:
            continue
        name = Path(match.group(2)).stem
        if name not in pages:
            continue
        categories.setdefault(category, []).append(name)
        summaries[name] = match.group(3).strip().rstrip(".")

    indexed = {name for names in categories.values() for name in names}
    additional = sorted(set(pages) - indexed)
    if additional:
        categories["Additional current instructions"] = additional
    return categories, summaries


def first_paragraph(text: str) -> str:
    introduction = re.search(
        r"## Introduction\s+(.+?)(?=\n## |\Z)", text, flags=re.DOTALL
    )
    if introduction:
        for paragraph in re.split(r"\n\s*\n", introduction.group(1).strip()):
            plain = " ".join(line.strip() for line in paragraph.splitlines())
            if plain and not plain.startswith(("```", "!!!", "|")):
                return re.sub(r"[`*_]", "", plain).rstrip(".")
    return "Current PTO instruction"


