def write_identity_pages(output: Path) -> None:
    thread_page = f"""# `get_thread_idx()`

{MARKER}

## Introduction

Return the immutable PE ID of the calling execution context. Every core has
four PEs, so the result is in `[0, 3]`.

## C++ Intrinsic

```cpp
PTO_INST uint32_t get_thread_idx();
```

## Constraints

- All four PEs start at the same program entry and initial PC.
- The return value is constant for the lifetime of a PE program instance.
- Branches on the result may diverge; each PE has its own architectural PC.
- The value does not address another PE's local tile register.

## Examples

```cpp
const uint32_t pe = get_thread_idx();
if (pe == 0) {{
  producer_path();
}} else {{
  consumer_path(pe);
}}
```
"""
    block_page = f"""# `get_block_idx()`

{MARKER}

## Introduction

Return the core ID of the current one-level program instance. The value is
shared by all four PEs in that core and is used to partition a global tensor.

## C++ Intrinsic

```cpp
PTO_INST uint32_t get_block_idx();
```

## Constraints

- All four PEs in a core observe the same value.
- The launch configuration defines the valid core-ID range.
- The intrinsic does not imply cross-core ordering or communication.
- Tensor slices selected by different cores must be disjoint unless the
  algorithm intentionally uses atomic memory operations.

## Examples

```cpp
const uint32_t core = get_block_idx();
const uint32_t row_begin = core * rows_per_core;
GlobalTensor block_slice = tensor.slice_rows(row_begin, rows_per_core);
TLOAD(local_tile, block_slice);
```
"""
    (output / "get_thread_idx.md").write_text(thread_page, encoding="utf-8")
    (output / "get_block_idx.md").write_text(block_page, encoding="utf-8")


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument(
        "--davincioo-root",
        type=Path,
        required=True,
        help="Path to a current DavinciOO checkout",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=Path(__file__).resolve().parents[1] / "docs" / "intrinsics",
    )
    args = parser.parse_args()

    root = args.davincioo_root.resolve()
    isa = root / "isa" / "pto"
    if not (isa / "README.md").is_file():
        raise SystemExit(f"not a DavinciOO ISA checkout: {root}")

    revision = git_output(root, "rev-parse", "HEAD")
    revision_date = git_output(root, "show", "-s", "--format=%cs", "HEAD")
    allowlist = read_allowlist()
    allowed = set(allowlist)
    davinci_pages = instruction_pages(isa)
    intrinsic_pages = instruction_pages(root / "isa" / "intrinsic")
    known_intrinsics = (
        set(davinci_pages)
        | set(intrinsic_pages)
        | set(SOURCE_ALIASES.values())
        | {"TEXPANDSCALAR", "MGATHER_MASK", "MSCATTER_MASK", "ACCCVT"}
    )
    pages: dict[str, Path] = {}
    for name in allowlist:
        source_name = SOURCE_ALIASES.get(name, name)
        source = davinci_pages.get(source_name)
        if source is None:
            source = SUPPLEMENTAL_DIR / f"{name.lower()}.md"
        if not source.is_file():
            raise SystemExit(f"LinxISA 0.57 intrinsic has no source page: {name}")
        pages[name] = source
    categories, summaries = parse_categories(isa / "README.md", pages)

    output = args.output.resolve()
    if output.exists():
        shutil.rmtree(output)
    output.mkdir(parents=True)

    for name, path in sorted(pages.items()):
        content = normalize_instruction(read(path), communication=False)
        source_name = SOURCE_ALIASES.get(name)
        if source_name:
            content = re.sub(rf"\b{re.escape(source_name)}\b", name, content)
        content = repair_instruction(name, content)
        content = eventless_contract(content)
        content = remove_subsection(content, "Manual")
        content = remove_subsection(content, "Manual Mode")
        content = strip_unsupported_intrinsics(
            content, allowed, known_intrinsics
        )
        content = group_level_contract(name, content)
        (output / f"{name.lower()}.md").write_text(content, encoding="utf-8")
        summaries.setdefault(name, first_paragraph(content))

    assembly_source = root / "isa" / "assembly" / "PTO-AS.md"
    if assembly_source.is_file():
        assembly = normalize_assembly(read(assembly_source))
        assembly = eventless_contract(assembly)
        assembly = tile_version_assembly_contract(assembly)
        assembly = shared_block_assembly_contract(assembly)
        assembly = strip_unsupported_intrinsics(
            assembly, allowed, known_intrinsics
        )
        (output / "assembly.md").write_text(assembly, encoding="utf-8")
    else:
        block_assembly_source = root / "isa" / "intrinsic" / "ASSEMBLY_SYNTAX.md"
        if not block_assembly_source.is_file():
            raise SystemExit(
                "DavinciOO checkout has neither isa/assembly/PTO-AS.md nor "
                "isa/intrinsic/ASSEMBLY_SYNTAX.md"
            )
        assembly = combined_assembly(read(block_assembly_source), revision)
        assembly = eventless_contract(assembly)
        assembly = tile_version_assembly_contract(assembly)
        assembly = shared_block_assembly_contract(assembly)
        assembly = strip_unsupported_intrinsics(
            assembly, allowed, known_intrinsics
        )
        (output / "assembly.md").write_text(assembly, encoding="utf-8")

    conventions = f"""# PTO 0.57 Common Conventions

{MARKER}

## Execution Group

A core contains four PEs. They begin at the same program entry and initial PC,
then execute independently. `get_thread_idx()` returns the PE ID and
`get_block_idx()` returns the core ID.

## Tile IDs and Versions

Every destination defines a new logical tile version. Source operands bind to
specific versions, and the superscalar machine issues an operation only after
those versions are ready. Physical register names and lifetime management are
not exposed to source code.

## PE-Local and Shared Tiles

PE-local tile registers are directly visible only to their owning PE. A shared
tile register is a core-local, versioned value with four fixed PE regions.
Shared definitions publish atomically after every defined region is ready.

## Shapes and Valid Regions

Tile shapes, layouts, locations, and valid regions are part of the operation
contract. An implementation may use padded physical storage, but it must not
read or write outside the logical valid region unless the intrinsic explicitly
defines padding behavior.

## Group Matrix Operations

The `TMATMUL` family consumes its right-hand matrix from a fully defined shared
tile register. Left and destination tiles are M-axis quarters held in PE-local
tile registers.

## Compiler Status

The shared tile-register forms are normative architecture contracts. Current
compiler builds may reject them until lowering and register allocation support
is implemented. Ordinary one-level benchmark forms remain compile checked.
"""
    (output / "conventions.md").write_text(conventions, encoding="utf-8")

    write_identity_pages(output)

    write_index(
        output,
        revision,
        revision_date,
        categories,
        summaries,
    )

    generated = {
        path.stem.upper()
        for path in output.glob("*.md")
        if INSTRUCTION_NAME.fullmatch(path.stem.upper())
    }
    if generated != allowed:
        raise SystemExit(
            f"generated intrinsic set mismatch: missing={sorted(allowed - generated)}, "
            f"extra={sorted(generated - allowed)}"
        )
    print(
        f"Generated {len(pages)} LinxISA PTO 0.57 pages plus 2 group identity "
        f"intrinsics from DavinciOO {revision}."
    )


if __name__ == "__main__":
    main()
