def eventless_contract(text: str) -> str:
    """Normalize the public 0.57 model to tile-ID dependency ordering."""
    text = re.sub(r",\s*typename\s*\.\.\.\s*WaitEvents", "", text)
    text = re.sub(r"typename\s*\.\.\.\s*WaitEvents\s*,\s*", "", text)
    text = re.sub(
        r",?\s*WaitEvents\s*&\s*\.\.\.\s*events", "", text
    )
    text = text.replace("PTO_INST RecordEvent", "PTO_INST void")
    text = re.sub(
        r"\n#{2,3} [^\n]*(?:Event|Synchronization)[^\n]*\n.*?(?=\n## |\Z)",
        "\n",
        text,
        flags=re.IGNORECASE | re.DOTALL,
    )
    lines = [
        line
        for line in text.splitlines()
        if not re.search(
            r"\b(?:RecordEvent|WaitEvents|TSYNC|events?)\b", line, re.IGNORECASE
        )
    ]
    return "\n".join(lines).rstrip() + "\n"


def strip_unsupported_intrinsics(
    text: str, allowed: set[str], known_intrinsics: set[str]
) -> str:
    for name in sorted(allowed, key=len, reverse=True):
        text = re.sub(rf"\b{re.escape(name)}_IMPL\b", name, text)

    def unsupported(value: str) -> bool:
        for token in re.findall(r"\b(?:T[A-Z0-9_]+|MGATHER|MSCATTER)\b", value):
            for name in known_intrinsics - allowed:
                if token == name or token.startswith(name + "_"):
                    return True
        return False

    def clean_fence(match: re.Match[str]) -> str:
        language, body = match.group(1), match.group(2)
        if language.lower() in {"cpp", "c++", "c", "cc"}:
            chunks = re.split(r"(?<=;)\n+", body)
            body = "\n".join(chunk for chunk in chunks if not unsupported(chunk))
        else:
            body = "\n".join(
                line for line in body.splitlines() if not unsupported(line)
            )
        return f"```{language}\n{body.rstrip()}\n```"

    text = re.sub(r"```([^\n]*)\n(.*?)```", clean_fence, text, flags=re.DOTALL)
    lines = []
    for line in text.splitlines():
        if unsupported(line):
            continue
        lines.append(line)
    return "\n".join(lines).rstrip() + "\n"


def replace_examples(text: str, body: str) -> str:
    replacement = "\n## Examples\n\n" + body.strip() + "\n"
    if re.search(r"\n## Examples?\s*\n", text):
        return re.sub(
            r"\n## Examples?\s*\n.*?(?=\n## |\Z)",
            replacement,
            text,
            count=1,
            flags=re.DOTALL,
        )
    return text.rstrip() + replacement


