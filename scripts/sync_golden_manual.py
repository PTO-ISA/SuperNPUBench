def rewrite_links(text: str, *, communication: bool) -> str:
    def replace(match: re.Match[str]) -> str:
        label, target = match.group(1), match.group(2)
        clean_target = target.split("#", 1)[0]
        anchor = "#" + target.split("#", 1)[1] if "#" in target else ""
        basename = Path(clean_target).name
        stem = Path(basename).stem

        if clean_target.endswith(("PTO-AS.md", "PTO-AS_zh.md")):
            return f"[{label}]({'../' if communication else ''}assembly.md{anchor})"
        if clean_target in {"../README.md", "README.md"} and communication:
            return f"[{label}](../index.md{anchor})"
        if clean_target == "comm/README.md":
            return f"[{label}](communication/index.md{anchor})"
        if INSTRUCTION_NAME.fullmatch(stem):
            prefix = "../" if communication and "../" in clean_target else ""
            return f"[{label}]({prefix}{stem.lower()}.md{anchor})"
        return match.group(0)

    return re.sub(r"\[([^]]+)\]\(([^)]+)\)", replace, text)


def rewrite_manual_paths(text: str, *, communication: bool) -> str:
    intrinsic_prefix = "../" if communication else ""
    model_prefix = "../../" if communication else "../"

    def instruction(match: re.Match[str]) -> str:
        name = match.group(1)
        return f"[`{name}`]({intrinsic_prefix}{name.lower()}.md)"

    text = re.sub(r"`docs/isa/([A-Z][A-Z0-9_]+)\.md`", instruction, text)
    text = text.replace(
        "`docs/isa/`", f"[this intrinsic manual]({intrinsic_prefix}index.md)"
    )
    text = text.replace(
        "`docs/coding/tutorials/`",
        f"[the tile-kernel tutorials]({model_prefix}tutorials/tile-kernel.md)",
    )
    text = text.replace(
        "`docs/coding/debug.md`",
        f"[troubleshooting]({model_prefix}start/troubleshooting.md)",
    )
    return text


